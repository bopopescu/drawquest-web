def _parse_ip(ip):
    return tuple(int(octet) for octet in ip.split('.'))

def is_bot(ip):
    try:
        parsed_ip = _parse_ip(ip)
    except ValueError:
        return False

    for bot_lower, bot_upper in bot_ranges:
        if bot_lower <= parsed_ip <= bot_upper:
            return True
    return False

